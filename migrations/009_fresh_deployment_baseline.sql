-- =====================================================
-- 2. Disambiguate the orchestrator tables
-- =====================================================
-- The orchestrator historically used tables named `projects` and `tasks`
-- (from init-db.sql) that collide with the v1 API's tables of the same
-- names. The orchestrator now uses orchestration_* names; rename existing
-- orchestrator-shape tables so their data is preserved.
DO $$
BEGIN
    IF EXISTS (SELECT 1 FROM information_schema.columns
               WHERE table_schema = 'public' AND table_name = 'projects'
                 AND column_name = 'project_id')
       AND NOT EXISTS (SELECT 1 FROM information_schema.tables
                       WHERE table_schema = 'public'
                         AND table_name = 'orchestration_projects') THEN
        ALTER TABLE projects RENAME TO orchestration_projects;
    END IF;

    IF EXISTS (SELECT 1 FROM information_schema.columns
               WHERE table_schema = 'public' AND table_name = 'tasks'
                 AND column_name = 'task_id')
       AND EXISTS (SELECT 1 FROM information_schema.columns
                   WHERE table_schema = 'public' AND table_name = 'tasks'
                     AND column_name = 'stage')
       AND NOT EXISTS (SELECT 1 FROM information_schema.tables
                       WHERE table_schema = 'public'
                         AND table_name = 'orchestration_tasks') THEN
        ALTER TABLE tasks RENAME TO orchestration_tasks;
    END IF;
END
$$;

