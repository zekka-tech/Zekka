-- =====================================================
-- 3. Repair uuid-lineage v1 tables (legacy 003/004, applied manually)
-- =====================================================
-- The v1 API code uses VARCHAR ids; uuid-shaped tables from the legacy
-- migrations never accepted the code's inserts, so they can only be empty.
-- Empty uuid-shaped tables are dropped and recreated below; non-empty ones
-- (hand-inserted data) are left for operator reconciliation.
DO $$
DECLARE
    t TEXT;
    id_type TEXT;
    row_count BIGINT;
BEGIN
    FOREACH t IN ARRAY ARRAY['message_reactions', 'messages', 'conversations',
                             'sources', 'project_members', 'projects'] LOOP
        SELECT data_type INTO id_type
        FROM information_schema.columns
        WHERE table_schema = 'public' AND table_name = t AND column_name = 'id';

        IF id_type = 'uuid' THEN
            EXECUTE format('SELECT COUNT(*) FROM %I', t) INTO row_count;
            IF row_count = 0 THEN
                EXECUTE format('DROP TABLE %I CASCADE', t);
                RAISE NOTICE 'Dropped empty uuid-lineage table % for recreation', t;
            ELSE
                RAISE NOTICE 'Table % has uuid ids and % rows — left untouched, manual reconciliation required', t, row_count;
            END IF;
        END IF;
    END LOOP;
END
$$;

