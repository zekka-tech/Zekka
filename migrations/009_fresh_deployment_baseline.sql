-- =====================================================
-- Migration: 009 - Fresh-Deployment Baseline & Schema Reconciliation
-- =====================================================
-- Description:
--   Makes `npm run migrate` on a pristine database produce a schema the
--   running code actually accepts, and reconciles the divergent schema
--   lineages that accumulated historically:
--     * init-db.sql        - orchestrator tables (projects keyed by project_id)
--     * create-minimal-*   - v1 API tables (VARCHAR ids, no users FKs)
--     * legacy 001-007     - aspirational UUID schema the code never matched
--     * runtime DDL        - users table self-created at boot
--   Legacy migrations 001-007 were retired to migrations/legacy/ — the
--   001→002 chain never completed on any database (002 conflicts with
--   001's api_keys), so no deployment depends on them running.
--
--   Everything here is idempotent and conditional, so it is safe on every
--   lineage: pristine DBs, init-db.sql compose deployments, runtime-
--   initialized DBs, and manually-applied legacy-migration DBs.
--
--   Identifier conventions (matching the code, not the legacy migrations):
--     * users.user_id / *_user_id columns: VARCHAR(64)  ('user_<ts>_<rand>')
--     * v1 entity ids (projects, conversations, ...): VARCHAR(255) (uuid strings)
--     * tenants ids: UUID (generated by the database, migration 008)
--     * No FKs to users: the users table shape varies by lineage, so the
--       application enforces that relationship.
-- Author: Zekka Framework Team
-- Date: 2026-07-19
-- Dependencies: 008_multi_tenancy.sql
-- =====================================================

